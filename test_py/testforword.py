import sys


def print_colored_banner(text, color):
    # ANSI转义序列开始部分
    start_escape = "\033["

    # 颜色代码
    color_code = {
        "black": "30",
        "red": "31",
        "green": "32",
        "yellow": "33",
        "blue": "34",
        "magenta": "35",
        "cyan": "36",
        "white": "37"
    }

    # 获取颜色代码
    color_value = color_code.get(color.lower(), "37")  # 默认为白色 (37)

    # 创建带颜色的横幅
    banner = f"{start_escape}47;{color_value}m{text}{start_escape}0m"

    # 打印带颜色的横幅
    print(banner)


